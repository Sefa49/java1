package day06_;

public class C01_Concatenation {
    public static void main(String[] args) {

        String a="Hello";
        int b =2;
        int c =3;

        System.out.println( a+b+c);
        System.out.println(c+b+a);
        System.out.println(a+(b+c));
        System.out.println(a+b*c);

    }





}
